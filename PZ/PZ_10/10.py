# В N колхозах выращивают некоторые сельскохозяйственные культуры из имеющегося
# перечня. Определить культуры, выращиваемые во всех колхозах, и культуры,
# выращиваемые только в некоторых из них.

# Пример данных: список колхозов и их культур
kolhozes = {
    "Колхоз 1": {"пшеница", "кукуруза", "картофель"},
    "Колхоз 2": {"пшеница", "ячмень", "картофель"},
    "Колхоз 3": {"пшеница", "картофель", "свекла"},
}

# Находим культуры, которые есть во всех колхозах
common_crops = set.intersection(*kolhozes.values())

# Находим культуры, которые есть только в некоторых колхозах
all_crops = set.union(*kolhozes.values())
some_crops = all_crops - common_crops

# Выводим результаты
print("Культуры, выращиваемые во всех колхозах:", common_crops)
print("Культуры, выращиваемые только в некоторых колхозах:", some_crops)
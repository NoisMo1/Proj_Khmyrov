# 1. В двумерном списке найти минимальный элемент в предпоследнем столбце.

matrix = [
    [5, 8, 3, 2],
    [6, 1, 9, 4],
    [7, 0, 2, 5]
]

# Получаем индекс предпоследнего столбца
pre_last_col = len(matrix[0]) - 2

# Собираем элементы предпоследнего столбца
elements = [row[pre_last_col] for row in matrix]

min_element = min(elements)
print(f"Минимальный элемент в предпоследнем столбце: {min_element}")
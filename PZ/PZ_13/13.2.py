# 2. Для каждой строки матрицы с нечетным номером найти среднее арифметическое ее
# элементов.

matrix = [
    [5, 8, 3],  # Индекс 0 (четная)
    [6, 1, 9],  # Индекс 1 (нечетная)
    [7, 0, 2],  # Индекс 2 (четная)
    [4, 5, 6]   # Индекс 3 (нечетная)
]

for i in range(len(matrix)):
    if i % 2 == 1:  # Проверяем нечетность индекса
        row = matrix[i]
        average = sum(row) / len(row)
        print(f"Строка {i}: Среднее = {average:.2f}")